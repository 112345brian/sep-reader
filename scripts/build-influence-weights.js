#!/usr/bin/env node
// Queries Wikidata for relationships between SEP-indexed thinkers and writes
// src/data/influenceWeights.ts, which the influence graph layout uses to pull
// closely associated philosophers together.
//
// Run whenever you want to refresh: node scripts/build-influence-weights.js
//
// Data source: Wikidata (CC0) via the public SPARQL endpoint.
// Relationships used:
//   P737  — influenced by  (weight 1.5)
//   P1066 — student of     (weight 3.0)
//   P802  — has student    (weight 3.0)
//
// The script is polite: one SPARQL call total, no per-entity fetches.

const https = require('https');
const fs = require('fs');
const path = require('path');

const OUT = path.join(__dirname, '../src/data/influenceWeights.ts');
const SPARQL_ENDPOINT = 'https://query.wikidata.org/sparql';
const UA = 'Nous/0.3 (SEP reader; influence weight builder; +https://github.com/112345brian/sep-reader)';

const REL_WEIGHT = {
  student_of:  3.0,
  has_student: 3.0,
  influenced_by: 1.5,
};

function sparqlQuery(query) {
  return new Promise((resolve, reject) => {
    const url = `${SPARQL_ENDPOINT}?query=${encodeURIComponent(query)}&format=json`;
    const req = https.get(url, {
      headers: { 'User-Agent': UA, Accept: 'application/sparql-results+json' },
    }, res => {
      const chunks = [];
      res.on('data', c => chunks.push(c));
      res.on('end', () => {
        try { resolve(JSON.parse(Buffer.concat(chunks).toString())); }
        catch (e) { reject(new Error(`JSON parse failed: ${e.message}`)); }
      });
    });
    req.on('error', reject);
    req.setTimeout(60_000, () => { req.destroy(); reject(new Error('SPARQL timeout')); });
  });
}

async function main() {
  console.log('Querying Wikidata for SEP thinker relationships…');

  // Single SPARQL call: all pairs of SEP-indexed entities linked by the three
  // relationship properties. Wikidata's P4223 holds the SEP entry slug.
  const query = `
SELECT ?sepId1 ?sepId2 ?rel WHERE {
  {
    ?e1 wdt:P1066 ?e2.
    BIND("student_of" AS ?rel)
  } UNION {
    ?e1 wdt:P802 ?e2.
    BIND("has_student" AS ?rel)
  } UNION {
    ?e1 wdt:P737 ?e2.
    BIND("influenced_by" AS ?rel)
  }
  ?e1 wdt:P4223 ?sepId1.
  ?e2 wdt:P4223 ?sepId2.
}
  `.trim();

  const result = await sparqlQuery(query);
  const bindings = result?.results?.bindings ?? [];
  console.log(`  ${bindings.length} relationships found.`);

  // weights[slugA][slugB] = strongest relationship weight in either direction.
  const weights = {};
  const bump = (a, b, w) => {
    if (!weights[a]) weights[a] = {};
    weights[a][b] = Math.max(weights[a][b] ?? 0, w);
  };

  for (const b of bindings) {
    const s1 = b.sepId1?.value;
    const s2 = b.sepId2?.value;
    if (!s1 || !s2 || s1 === s2) continue;
    const w = REL_WEIGHT[b.rel?.value] ?? 1.0;
    bump(s1, s2, w);
    // Reverse direction at slightly lower weight so the pair pulls together
    // regardless of which node is the center of the current graph.
    bump(s2, s1, w * 0.8);
  }

  const thinkerCount = Object.keys(weights).length;
  const pairCount = Object.values(weights).reduce((s, m) => s + Object.keys(m).length, 0);
  console.log(`  ${thinkerCount} thinkers, ${pairCount} directed pairs.`);

  const ts = `\
// Auto-generated by scripts/build-influence-weights.js — do not edit by hand.
// Source: Wikidata CC0 (P737 influenced_by, P1066 student_of, P802 has_student)
// mapped via P4223 (SEP identifier).  Refresh: node scripts/build-influence-weights.js
export const INFLUENCE_WEIGHTS: Record<string, Record<string, number>> =
${JSON.stringify(weights, null, 2)};

/** Highest weight for the pair (slugA, slugB) in either direction; 1.0 if unknown. */
export function getInfluenceWeight(slugA: string, slugB: string): number {
  return INFLUENCE_WEIGHTS[slugA]?.[slugB]
    ?? INFLUENCE_WEIGHTS[slugB]?.[slugA]
    ?? 1.0;
}
`;

  fs.writeFileSync(OUT, ts, 'utf8');
  console.log(`Written → ${path.relative(process.cwd(), OUT)}`);
}

main().catch(e => { console.error(e); process.exit(1); });
